    private void btnCompararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompararActionPerformed
        String nombre1 = (String) cmbPais1.getSelectedItem();
        String nombre2 = (String) cmbPais2.getSelectedItem();
        
        Pais p1 = buscarPais(nombre1);
        Pais p2 = buscarPais(nombre2);
        
        DefaultTableModel model = (DefaultTableModel) tblComparacion.getModel();
        model.setRowCount(0);
        
        if (p1 != null && p2 != null) {
            model.addRow(new Object[]{"Nombre", p1.getNombre(), p2.getNombre()});
            model.addRow(new Object[]{"Continente", p1.getContinente(), p2.getContinente()});
            model.addRow(new Object[]{"Población", p1.getPoblacion(), p2.getPoblacion()});
            model.addRow(new Object[]{"Gobierno", p1.getTipoGobiernoTexto(), p2.getTipoGobiernoTexto()});
        }
    }//GEN-LAST:event_btnCompararActionPerformed

    private Pais buscarPais(String nombre) {
        for (Pais p : paisDAO.listarPaises()) {
            if (p.getNombre().equals(nombre)) {
                return p;
            }
        }
        return null;
    }

