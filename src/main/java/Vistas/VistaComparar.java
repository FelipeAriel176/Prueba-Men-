    private void cargarPaises() {
        ArrayList<Pais> paises = paisDAO.listarPaises();
        cmbPais1.removeAllItems();
        cmbPais2.removeAllItems();
        for (Pais p : paises) {
            cmbPais1.addItem(p.getNombre());
            cmbPais2.addItem(p.getNombre());
        }
    }

